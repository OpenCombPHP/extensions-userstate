class FaceIcon{
    
        public function toPic($tag,$service)
        {
            return $this::$aFaceIcon[$service][$tag];
        }
        public function toTag($tag,$service)
        {
            return empty($this::$aForward[$tag][$service])?$tag:$this::$aForward[$tag][$service];
        }
        public function changePic($title,$service)
        {
            $aFaceIcon = $this::$aFaceIcon[$service];
            foreach ($aFaceIcon as $k => $v){
                $aSource[] = $k;
                $aTarget[] = '<img src="'.$v.'">';
            }
            $title = str_replace($aSource, $aTarget, $title);
            return $title;
        }
        public function changeTag($title,$service)
        {
            
            foreach (self::$aTag as $kk => $vv)
            {
                $aFaceIcon = $this::$aFaceIcon[$kk];
                foreach ($aFaceIcon as $k => $v){
                    $aSource[] = $k;
                    $aTarget[] = '{~'.$k.'~}';
                }
                $title = str_replace($aSource, $aTarget, $title);
                $aSource = array();
                $aTarget = array();
            }
            preg_match_all("/\{~(.*?)~\}/", $title, $aTags);
            
            if(!empty($aTags[1]))
            {
                for($i = 0; $i < sizeof($aTags[1]); $i++){
                    if(!empty(self::$aForward[$aTags[1][$i]][$service]))
                    {
                        $title = str_replace($aTags[1][$i], self::$aForward[$aTags[1][$i]][$service] , $title);
                    }
                }
            }
            return preg_replace("/\{~(.*?)~\}/u", '$1', $title);
        }
        
